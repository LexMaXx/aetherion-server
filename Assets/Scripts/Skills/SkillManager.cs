/// –ú–µ–Ω–µ–¥–∂–µ—Ä —Å–∫–∏–ª–ª–æ–≤ –ø–µ—Ä—Å–æ–Ω–∞–∂–∞ (–∑–∞–≥—Ä—É–∂–∞–µ—Ç—Å—è –≤ Arena)

    {
        Debug.Log("[NetworkCombatSync] üíÄ –ò–≥—Ä–æ–∫ –ø–æ–≥–∏–±");

        // –°–µ—Ä–≤–µ—Ä —É–∂–µ –∑–Ω–∞–µ—Ç –æ –Ω–∞—à–µ–π —Å–º–µ—Ä—Ç–∏ —á–µ—Ä–µ–∑ health update
